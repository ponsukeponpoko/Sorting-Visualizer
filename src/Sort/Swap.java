package Sort;

public class Swap {
	int _lowerIndex;
	int _val1;
	int _val2;
	boolean _bool;

    public Swap(int lowerIndex, int val1, int val2, boolean isSwapped) {
        this._lowerIndex = lowerIndex;
        this._val1 = val1;
        this._val2 = val2;
        this._bool = isSwapped;
    }
}
